-- 6. Row Level Security (RLS) Policies
-- Enable RLS on all tables
ALTER TABLE products ENABLE ROW LEVEL SECURITY;
ALTER TABLE categories ENABLE ROW LEVEL SECURITY;
ALTER TABLE orders ENABLE ROW LEVEL SECURITY;
ALTER TABLE custom_skin_requests ENABLE ROW LEVEL SECURITY;
ALTER TABLE settings ENABLE ROW LEVEL SECURITY;

-- Products: Everyone can read visible products, only authenticated admins can write/update all
CREATE POLICY "Public read visible products" ON products FOR SELECT USING (is_visible = true OR auth.role() = 'authenticated');
CREATE POLICY "Admins can manage products" ON products FOR ALL USING (auth.role() = 'authenticated');

-- Categories: Everyone can read, admins can manage
CREATE POLICY "Public read categories" ON categories FOR SELECT USING (true);
CREATE POLICY "Admins can manage categories" ON categories FOR ALL USING (auth.role() = 'authenticated');

-- Orders & Requests: Public can insert (reserve), admins can read/update/delete
CREATE POLICY "Public insert orders" ON orders FOR INSERT WITH CHECK (true);
CREATE POLICY "Admins manage orders" ON orders FOR ALL USING (auth.role() = 'authenticated');

CREATE POLICY "Public insert skin requests" ON custom_skin_requests FOR INSERT WITH CHECK (true);
CREATE POLICY "Admins manage skin requests" ON custom_skin_requests FOR ALL USING (auth.role() = 'authenticated');

-- Settings: Public can read, admins can manage
CREATE POLICY "Public read settings" ON settings FOR SELECT USING (true);
CREATE POLICY "Admins manage settings" ON settings FOR ALL USING (auth.role() = 'authenticated');

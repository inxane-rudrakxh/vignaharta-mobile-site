-- Vighnaharta Mobile Shop Database Schema

-- 1. Create custom_skin_requests table
CREATE TABLE IF NOT EXISTS custom_skin_requests (
  id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  device_type text NOT NULL,
  skin_type text NOT NULL,
  payment_type text NOT NULL,
  uploaded_design_url text,
  customization_notes text,
  customer_name text NOT NULL,
  customer_phone text,
  payment_status text DEFAULT 'Pending',
  order_status text DEFAULT 'Pending',
  admin_notes text,
  razorpay_payment_id text,
  razorpay_order_id text,
  razorpay_signature text,
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 2. Create products table (if missing in previous script)
CREATE TABLE IF NOT EXISTS products (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title text NOT NULL,
  price numeric NOT NULL,
  category text,
  stock_status text,
  stock_quantity integer DEFAULT 0,
  description text,
  images jsonb,
  image text,
  featured boolean DEFAULT false,
  is_visible boolean DEFAULT true,
  rating numeric DEFAULT 5.0,
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 3. Create orders table for product reservations
CREATE TABLE IF NOT EXISTS orders (
  id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  product_name text,
  customer_name text NOT NULL,
  customer_phone text NOT NULL,
  customer_email text,
  payment_type text NOT NULL, -- 'Full Payment' or 'Advance Payment'
  amount_paid numeric DEFAULT 0,
  order_status text DEFAULT 'Pending', -- Pending, Reserved, Processing, Ready for Pickup, Picked Up, Completed, Cancelled
  pickup_status text DEFAULT 'Pending',
  razorpay_payment_id text,
  razorpay_order_id text,
  razorpay_signature text,
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 4. Create categories table
CREATE TABLE IF NOT EXISTS categories (
  id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  name text NOT NULL UNIQUE,
  image text,
  featured boolean DEFAULT false,
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

